//logical operators

// equality
2 == 2    // true
2 == '2'  // true

// inequality
2 != 2    // false
2 != '2'  // false

// strict equality
2 === 2   // true
2 === '2' // false

 // strict inequality
2 !== 2   // false
2 !== '2' // true

// greater than
3 > 2   // true

// less than 
3 < 5   // true

// greater than or equal to
3 >= 3  // true

// less than or equal to
3 <= 3  // true